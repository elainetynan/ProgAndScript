# Set variables
i = 5
fl = 1.2
isa = False
memo = 'Elaine'
lots = [1,2,3,4,5]

# Print types
print("Variable i is of type: {} And value {}".format(type(i), i))
print("Variable fl is of type: {} And value {}".format(type(fl), fl))
print("Variable isa is of type: {} And value {}".format(type(isa), isa))
print("Variable memo is of type: {} And value {}".format(type(memo), memo))
print("Variable lots is of type: {} And value {}".format(type(lots), lots))